CREATE TABLE Studios (
    studio_id CHAR(10) PRIMARY KEY,
    name VARCHAR(100),
    country VARCHAR(50)
);

CREATE TABLE Genres (
    genre_id CHAR(10) PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE Directors (
    director_id CHAR(10) PRIMARY KEY,
    name VARCHAR(100),
    dob DATE,
    nationality VARCHAR(40)
);

CREATE TABLE Actors (
    actor_id CHAR(10) PRIMARY KEY,
    name VARCHAR(100),
    dob DATE,
    nationality VARCHAR(40)
);

CREATE TABLE Movies (
    movie_id CHAR(8) PRIMARY KEY,
    title VARCHAR(125),
    year SMALLINT,
    revenue_generated INT,
    runtime SMALLINT,
    rating DECIMAL(3,1),
    short_description VARCHAR(500),
    director_id CHAR(10) REFERENCES Directors(director_id),
    studio_id CHAR(10) REFERENCES Studios(studio_id),
    genre_id CHAR(10) REFERENCES Genres(genre_id)
);

CREATE TABLE Movie_Cast (
    movie_id CHAR(8) REFERENCES Movies(movie_id),
    actor_id CHAR(10) REFERENCES Actors(actor_id),
    role VARCHAR(100),
    PRIMARY KEY (movie_id, actor_id)
);

